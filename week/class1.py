# 구구단 출력하기 : for
# =============================================================================

def test1():
    for dan in range (2,10):
        for i in range(1,10):
            # print(dan,'*',i,'=',dan*i)
            # print("%d * %d = %d" %(dan, i ,dan*i))
            print("{} * {}  = {}".format(dan, i, dan*i))
