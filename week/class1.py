# 구구단 출력하기 : while 
# =============================================================================

def test2():
    dan = 2
    while dan < 10:
        i = 1
        while i < 10:
            print("{} * {} = {}".format(dan, i, dan*i))
            i+=1
        dan+=1
            
        
